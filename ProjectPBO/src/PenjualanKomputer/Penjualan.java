    private void TabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelMouseClicked
        // TODO add your handling code here:
        int baris = Tabel.rowAtPoint(evt.getPoint());
        String Beli = Tabel.getValueAt(baris, 1).toString();
        txtBeli.setText(Beli);
        
        String Barang = Tabel.getValueAt(baris, 2).toString();
        txtBarang.setText(Barang);
        
        String Jumlah = Tabel.getValueAt(baris, 4).toString();
        txtJum.setText(Jumlah);
        
        try {
            DefaultTableModel model = (DefaultTableModel)Tabel.getModel();
            Date tgl = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(baris, 5).toString());
            dateJual.setDate(tgl);
            
        } catch (ParseException ex) {
            Logger.getLogger(Penjualan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_TabelMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "INSERT INTO penjualan VALUES (?, ?, ?, ?)";
            Connection conn = (Connection)Config.configDB();
            PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, txtBeli.getText());
            pstm.setString(2, txtBarang.getText());
            pstm.setInt(3, Integer.parseInt(txtJum.getText()));
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tgl = sdf.format(dateJual.getDate());
            pstm.setString(4, tgl);
            
            pstm.execute();
            
            
            JOptionPane.showMessageDialog(null, "Simpan Berhasil!", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);
            tampilkan_data();
            kosong_form();
                    
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

