    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID Pembeli.");
        model.addColumn("ID Barang.");
        model.addColumn("Nama Barang.");
        model.addColumn("Unit.");
        model.addColumn("Tanggal Beli.");
        model.addColumn("Total Beli.");
        
        
        try{
            int no = 1;
            String sql = "SELECT pn.id_pembeli, pn.id_barang, br.nama_barang, pn.jum_barang, pn.tgl_jual, SUM(pn.jum_barang*br.harga_brg) FROM penjualan pn, barang br WHERE br.id_barang=pn.id_barang GROUP BY id_pembeli";
            Connection conn = (Connection)Config.configDB(); 
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),"Rp. "+res.getString(6)});
                
            }
            Tabel.setModel(model);
            
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
